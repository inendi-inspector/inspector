#include <stdlib.h>
#include <string.h>

#include <picviz/plugins.h>


char * picviz_plugins_get_functions_dir(void)
{
	char *pluginsdir;

	pluginsdir = getenv("PICVIZ_FUNCTIONS_DIR");
	if ( ! pluginsdir ) {
		pluginsdir = PICVIZ_FUNCTIONS_DIR; /* Variable generated by CMAKE */
	}

	return pluginsdir;
}


